document.addEventListener('DOMContentLoaded', function() {
    // Poema para tu novia
    const poema = `
        Eres la luz que ilumina mi camino,
        la melodía que alegra mi corazón,
        el susurro que calma mi alma.
        
        En cada latido te siento cerca,
        en cada pensamiento te encuentro presente,
        y en cada sueño, contigo quiero estar.

        Tú eres mi amor, mi vida, mi todo,
        y en esta página te lo digo con amor infinito.
    `;

    // Insertar el poema dentro del elemento con id "poema"
    document.getElementById('poema').innerHTML = poema;
});
